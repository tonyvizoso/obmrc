<p>When we hear our C.E.O., Yanet R. Ferrera talking about the beginning, growth and projections, we immediately realize that  the success of a business comes of the hands of a great discipline and dedication.</p>
<p>Even more, to build it from scratch takes courage and a lot of sacrifice.</p>
<p>Well, you may say: “many business people share almost the same history” and indeed that’s right, it's the history of business people, but there is a tweak in every tale.</p>

<p>In ours there is a particular human factor:<br>
Being a woman and building from scratch a business that employs around twenty people, most of them healthcare professionals, make this an extraordinary case, because it tells us about the tenacity of a young woman as an outstanding entrepreneur.</p>
 
<p>And it doesn't mean that Yanet R. Ferrera is out of this world or over the rest of us. It means she is beyond the edge, that she belongs already to the new age where women share important responsibilities as providers in modern society.</p>

<p>Her vision, preparedness, work results and her capacity to share with her employees the growth of her business, set an example of an extraordinary capacity as human being, first.</p>
 
<p>When she talks about her business, we see her deep, dark eyes shining like mirrors while transpiring passion and a big blast from her dreams wrap us in enthusiasm, to continue with her the big jump to the future.</p>

<p>She always says: "if Ocean Blue grows, my employees can grow with me too!”</p>
